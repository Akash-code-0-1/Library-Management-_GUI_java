    private void rSMaterialButtonCircle4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle4ActionPerformed
        // TODO add your handling code here:
        
        String SI= studentId.getText();
        String SN= studentName.getText();
        
        Object selectedValue1 = course.getSelectedItem();
        Object selectedValue2 = department.getSelectedItem();
        
        //inserting value in the database table student table
        String sql= "UPDATE student_info SET student_name=?,course=?,department=? WHERE student_id=?";
        
        try{
              
         
            Statement s= AkashdbsqlConnect.ConnectedDB().createStatement();
            

            PreparedStatement statement= cn.prepareStatement(sql);
            
            
            statement.setString(1, SN);
            statement.setObject(2, selectedValue1);
            statement.setObject(3, selectedValue2);
            statement.setString(4, SI);
            


            int rowsUpdated = statement.executeUpdate();
            
            if (rowsUpdated > 0) {
                
                JOptionPane.showMessageDialog(rootPane, "Data Updated Successfully!");
                
            }
             statement.close(); // Close the statement when done.
            } catch (SQLException e) {
                      e.printStackTrace();
            }
        
        
        ManageStudents mb=new ManageStudents();
         this.dispose();
         mb.setVisible(true);
            

        
        

    }//GEN-LAST:event_rSMaterialButtonCircle4ActionPerformed

