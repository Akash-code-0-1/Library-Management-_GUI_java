        public void setStudentDetailsToTable(){
        
        try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/akashdb","root","Akash360@");
            
            Statement st=cn.createStatement();
            ResultSet rs= st.executeQuery("select * from student_info");
            
            while(rs.next()){
                
                String SI=rs.getString("student_id");
                String SN= rs.getString("student_name");
                Object selectedValue1 = rs.getString("course");
                Object selectedValue2 = rs.getString("department");

                
                Object[] obj= {SI,SN,selectedValue1,selectedValue2};
                
                model=(DefaultTableModel) tbl_student_details.getModel();
                model.addRow(obj);
            }
            
        
    }catch(Exception e){
        e.printStackTrace();
        
    }
    }
    
    
    
    
    

