    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                String un = jname.getText();
                String ps = jpass.getText();

        try {
            String sql = "SELECT * FROM user WHERE User_Name=? AND Password=?";
            pst = cn.prepareCall(sql);

            pst.setString(1, un); // user name
            pst.setString(2, ps); // password

            rs = pst.executeQuery();

            if (rs.next()) {

                JOptionPane.showMessageDialog(rootPane, "Successfully Loged In !");
                
                new AdminPanel().setVisible(true);
                this.dispose();
                
            } else {

                JOptionPane.showMessageDialog(rootPane, "Invalid Password or Username!!");
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

